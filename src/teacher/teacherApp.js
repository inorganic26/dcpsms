                  // console.log("[teacherApp.js] Calling lessonManager.handleLessonFilterChange..."); // Î°úÍ∑∏ ÌïÑÏöîÏãú Ï£ºÏÑù Ìï¥Ï†ú
                  this.lessonManager.handleLessonFilterChange(); // lessonManager.jsÏùò wrapper Ìò∏Ï∂ú
